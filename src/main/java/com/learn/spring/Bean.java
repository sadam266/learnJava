package com.learn.spring;

public class Bean {

    private int anInt;

    private String string;

    public Bean() {}

    public int getAnInt() {
        return anInt;
    }

    public void setAnInt(int anInt) {
        this.anInt = anInt;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }
}
